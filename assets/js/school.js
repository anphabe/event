window.onload = function () {

    //Get html elements
    var countySel = document.getElementById("city");
    var stateSel = document.getElementById("school");


    //Load countries
    for (var country in countryStateInfo) {
        countySel.options[countySel.options.length] = new Option(country, country);
    }

    //County Changed
    countySel.onchange = function () {

        stateSel.length = 1; // remove all options bar first
        if (this.selectedIndex < 1)
            return; // done
        for (var state in countryStateInfo[this.value]) {
            stateSel.options[stateSel.options.length] = new Option(countryStateInfo[this.value][state], countryStateInfo[this.value][state]);
        }
    }

}
